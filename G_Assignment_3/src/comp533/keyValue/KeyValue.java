package comp533.keyValue;

public class KeyValue<K, V> implements KeyValueInterface<K, V> {
    private K key;
    private V value;

    public KeyValue(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public V getValue() {
        return value;
    }

    @Override
    public String toString() {
        return "(" + this.key + "," + this.value + ")";
    }
}
